@push('js')
    <script>
        $(document).ready(function () {
            var dataLevel = $('#table_kategori').DataTable({
                processing: true,
                serverSide: true,
                ajax: {
                    url: "{{ url('kategori/list') }}",
                    type: "POST",
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    },
                    data: function (d) {
                        d.kategori_kode = $('#kategori_kode').val(); // Kirim nilai filter kategori_kode
                    }
                },
                columns: [
                    {
                        data: "DT_RowIndex",
                        className: "text-center",
                        orderable: false,
                        searchable: false,
                    },
                    {
                        data: "kategori_kode",
                        orderable: true,
                        searchable: true
                    },
                    {
                        data: "kategori_nama",
                        orderable: true,
                        searchable: true
                    },
                    {
                        data: "aksi",
                        className: "text-center",
                        orderable: false,
                        searchable: false
                    }
                ]
            });

            // Event listener untuk filter kategori
            $('#kategori_kode').on('change', function () {
                dataLevel.ajax.reload();
            });
        });

    </script>
